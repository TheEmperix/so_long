#include "ft_printf.h"

void	ft_putstr_fd(char *s, int *fd)
{
	int	i;

	i = 0;
	if (s == 0)
		*fd += write(1, "(null)", 6);
	else
	{
		while (s[i] != '\0')
		{
			*fd += write(1, &s[i], 1);
			i++;
		}
	}
}
